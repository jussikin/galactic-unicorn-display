/// Galactic Unicorn 53×11 LED matrix driver.
///
/// Hardware: shift-register chain driven by PIO.
/// GPIO assignments (from Pimoroni galactic_unicorn.cpp):
///   CLK   = GPIO 13
///   DATA  = GPIO 14
///   LATCH = GPIO 15
///   BLANK = GPIO 16
///   ROW0  = GPIO 17, ROW1 = GPIO 18, ROW2 = GPIO 19, ROW3 = GPIO 20
///
/// Each row is serialised as 53 × 3 bytes (R,G,B) into the shift registers,
/// then latched.  The PIO program handles the bit-banging at a fixed frequency
/// so the CPU just writes words into the FIFO.


pub const WIDTH: usize = 53;
pub const HEIGHT: usize = 11;

pub struct Display {
    // Frame buffer: [row][col] = (r, g, b)
    buf: [[(u8, u8, u8); WIDTH]; HEIGHT],
    brightness: f32,
}

impl Display {
    pub fn new(brightness: f32) -> Self {
        Self {
            buf: [[(0, 0, 0); WIDTH]; HEIGHT],
            brightness: brightness.clamp(0.0, 1.0),
        }
    }

    pub fn clear(&mut self) {
        for row in self.buf.iter_mut() {
            for px in row.iter_mut() {
                *px = (0, 0, 0);
            }
        }
    }

    /// Set a single pixel. Coordinates are clamped silently.
    pub fn set_pixel(&mut self, x: usize, y: usize, r: u8, g: u8, b: u8) {
        if x < WIDTH && y < HEIGHT {
            self.buf[y][x] = (r, g, b);
        }
    }

    /// Draw a single ASCII character using the 5×7 bitmap font.
    /// Returns the x-advance (pixels consumed).
    pub fn draw_char(&mut self, x: i32, y: i32, ch: char, r: u8, g: u8, b: u8) -> i32 {
        let glyph = font_glyph(ch);
        for (row, bits) in glyph.iter().enumerate() {
            for col in 0..5usize {
                if bits & (1 << (4 - col)) != 0 {
                    let px = x + col as i32;
                    let py = y + row as i32;
                    if px >= 0 && px < WIDTH as i32 && py >= 0 && py < HEIGHT as i32 {
                        self.set_pixel(px as usize, py as usize, r, g, b);
                    }
                }
            }
        }
        6 // 5px glyph + 1px gap
    }

    /// Draw a string, returns total pixel width.
    pub fn draw_str(&mut self, x: i32, y: i32, s: &str, r: u8, g: u8, b: u8) -> i32 {
        let mut cx = x;
        for ch in s.chars() {
            cx += self.draw_char(cx, y, ch, r, g, b);
        }
        cx - x
    }

    /// Measure a string width in pixels without drawing.
    pub fn measure_str(s: &str) -> i32 {
        s.len() as i32 * 6
    }

    /// Apply brightness scaling and return the raw buffer (consumed by the PIO driver).
    pub fn frame(&self) -> [[(u8, u8, u8); WIDTH]; HEIGHT] {
        let scale = self.brightness;
        let mut out = [[(0u8, 0u8, 0u8); WIDTH]; HEIGHT];
        for y in 0..HEIGHT {
            for x in 0..WIDTH {
                let (r, g, b) = self.buf[y][x];
                out[y][x] = (
                    (r as f32 * scale) as u8,
                    (g as f32 * scale) as u8,
                    (b as f32 * scale) as u8,
                );
            }
        }
        out
    }
}

// ---------------------------------------------------------------------------
// Minimal 5×7 ASCII bitmap font (printable ASCII 0x20–0x7e)
// Each entry is 7 bytes; each byte is a 5-bit row (MSB = leftmost pixel).
// ---------------------------------------------------------------------------

fn font_glyph(ch: char) -> [u8; 7] {
    let idx = ch as usize;
    if idx < 0x20 || idx > 0x7e {
        return FONT[0]; // space
    }
    FONT[idx - 0x20]
}

